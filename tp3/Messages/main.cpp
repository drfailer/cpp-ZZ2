#include <iostream>
#include "A.hpp"
#include "B.hpp"

/******************************************************************************/
/*                                  classe A                                  */
/******************************************************************************/

A::A(int i): i(i) {}

void A::send(B* b)
{
  b->exec(i);
}

void A::exec(int newi)
{
  i = newi;
}

/******************************************************************************/
/*                                  classe B                                  */
/******************************************************************************/


B::B(int j): j(j) {}

void B::send(A *a)
{
  a->exec(j);
}

void B::exec(int newj)
{
  j = newj;
}

/******************************************************************************/
/*                                    main                                    */
/******************************************************************************/

int main(int, char**)
{
  A unA(1);
  B unB(1);
  unA.send(&unB);
  return 0;
}
